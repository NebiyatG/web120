<h3>Write:</h3>
<p>A parallax scroll occurs when content (background) scrolls at different speeds than the rest of the page. This creates an appealing effect and lengthens the webpage, allowing for more content. The word parallax originally comes from video games which often moved the background of the game slower than the foreground in order to create an appealing effect for players. This is the idea behind parallax scrolling. Parallax scroll keeps visitors entertained and engaged in the website. It can help take a storytelling approach to guide visitors through the website. Additionally, using parallax scrolling can reinforce website credibility because it shows that the website designer knows how to perform advanced functions. While there are many benefits to parallax scrolling, there are also a few downsides. According to the unleashes technologies website, one downside is that scrolling allows designers to put a great deal of information on one page and users can scroll through that whole page. However, sometimes having tonsof information might cause slower load times for visitors. Nonetheless, parallax scrolling has become popular among many different users, both individuals and companies. Specifically, there are four websites that have been known to utilize parallax scrolling well. These websites are: Nintendo Game Bo, Soleil Noir, and The New York times for for of their articles titled “Tomato Can Blues.” Nintendo uses parallax scrolling to make players feel as though they are playing Tetris on an old Gameboy. This gives their game an authentic feel which is attractive for players. There are 4 ways to implement parallax scrolling. The raster parallax gives the illusion of movement, the repeat pattern uses scrolling to display content over the background, the layered parallax allows the background and foreground to move at different paces and lastly the sprite method, which uses sprites (2D bitmaps) which are individually controllable and drawn by hardware.
